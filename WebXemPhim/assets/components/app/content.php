    for(i = 0;i<totalCarousel ; i++)
    {
        count[i] = 0;
    }

    function hideBtn() {
        for(var i = 0; i<totalCarousel ; i++)
        {
            if(count[i] == 0){                    
                prevBtns[i].style.display = 'none'                    
            }
        }
    }


    nextBtns.forEach(function(item,index) {
        item.onclick = function(){
            count[index]++
