        navbar.classList.remove("header__navbar--vertical"); 
        navIconClose.style.display = "none";
        navIconOpen.style.display = "inline-block";
        navBackground.style.transform = "translateY(calc(-100% - 69px))";
        navBackground.style.opacity = "0";
    })
</script>